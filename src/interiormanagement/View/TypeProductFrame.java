    private void setButtonEnable(boolean update,boolean delete){
        updateButton.setEnabled(update);
        deleteButton.setEnabled(delete);
    }
    private void ShowDataTable(){
        model.setRowCount(0);
        typeProducts.clear();
        resultSet = ContentProvider.getList(TypeProduct.TABLE_NAME);

        try {
            while(resultSet.next()){
                typeProducts.add(new TypeProduct(resultSet.getInt("MALOAI"), resultSet.getNString("TENLOAI")));
            }
        } catch (SQLException ex) {
            Logger.getLogger(TypeProductFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        AddRow();
        process.CloseConnect();
    }
    
    private void AddRow(){
        int stt = 1;
        for(TypeProduct type : typeProducts){
            model.addRow(new Object[]{
                stt,
                type.getTenLoai()
            });
            stt++;
        }
    }
    
    private boolean InsertType(){
        if (process.Connect()) {
           return process.InsertType(tfTypeName.getText());
        }
        return false;
    }
    
    private boolean UpdateType(){
     if (process.Connect()) {
           return process.UpdateType(typeProducts.get(typeProductTable.getSelectedRow()).getMaLoai(),tfTypeName.getText());
        }
        return false;
    }
    
    private boolean DeleteType(){
        if (process.Connect()) {
           return process.DeleteData(TypeProduct.TABLE_NAME,"MALOAI",String.valueOf(typeProducts.get(typeProductTable.getSelectedRow()).getMaLoai()));
        }
        return false;
    }
