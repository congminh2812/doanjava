    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
       boolean check = false;
        for (TypeProduct type : typeProducts) {
            if (tfTypeName.getText().toLowerCase().equals(type.getTenLoai().toLowerCase())) {
                check = true;
            }
        }
        if (!check) {
            if (InsertType()) {
            JOptionPane.showMessageDialog(centerPanel, "Thêm Thành Công!");
            tfTypeName.setText("");
            ShowDataTable();
            }else
                JOptionPane.showMessageDialog(centerPanel, "Thêm Thất Bại!");
        }else
            JOptionPane.showMessageDialog(centerPanel,"Loại Sản Phẩm đã tồn tại!");
       

    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        boolean check = false;
        for (TypeProduct type : typeProducts) {
            if (tfTypeName.getText().toLowerCase().equals(type.getTenLoai().toLowerCase())) {
                check = true;
            }
        } 
        if (UpdateType() && !check) {
            JOptionPane.showMessageDialog(centerPanel, "Sửa Thành Công!");
            tfTypeName.setText("");
            ShowDataTable();
            setButtonEnable(false, false);
         }else
            JOptionPane.showMessageDialog(centerPanel, "Sửa Thất Bại!");
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int result = JOptionPane.showConfirmDialog(centerPanel, "Bạn có chắc muốn xóa không?","Thông báo",JOptionPane.YES_NO_OPTION);
        if (result == 0) {
            if (DeleteType()) {
            ShowDataTable();
            setButtonEnable(false, false);
            tfTypeName.setText("");
        }else
            JOptionPane.showMessageDialog(centerPanel, "Loại này còn tồn tại trong kho !");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

