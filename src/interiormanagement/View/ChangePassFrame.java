    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        if (validateInfo()) {
            JOptionPane.showMessageDialog(centerPanel,"Bạn chưa nhập đủ thông tin!");
        }else{
          if (tfPassOld.getText().equals(nhanvien.getPass())) {
            if (tfPassNew.getText().equals(tfPassNewAgain.getText())) {
                if (ChangePassword()) {
                    JOptionPane.showMessageDialog(centerPanel,"Đổi mật khẩu thành công!");
                    setVisible(false);
                }else
                    JOptionPane.showMessageDialog(centerPanel,"Đổi mật khẩu thất bại!");
                
                process.CloseConnect();
            }else
                JOptionPane.showMessageDialog(centerPanel,"Mật khẩu nhập lại không trùng khớp !");
        }else
            JOptionPane.showMessageDialog(centerPanel,"Mật khẩu cũ sai!");
        }
    }//GEN-LAST:event_acceptButtonActionPerformed

