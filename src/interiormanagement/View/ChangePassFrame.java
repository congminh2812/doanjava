    private boolean ChangePassword(){
        if (process.Connect()) {
            return process.ChangePassword(nhanvien.getIdEmployee(), tfPassNew.getText());
        }
        return false;
    }
    
    private boolean validateInfo(){
        if (tfPassOld.getText().equals("") || tfPassNew.getText().equals("") || tfPassNewAgain.getText().equals("")) {
            return true;
        }
        return false;
    }
